$(document).ready(()=>{
    $(".btn1").click(()=>{
        $(".popmenu_b1").fadeIn();
        $("#body").css("background-color","rgba(0, 0, 0, 0.2)");
    });
    $(".exit_b1").click(()=>{
        $(".popmenu_b1").fadeOut();
        $("#body").css("background-color","#fff");

    });
});

